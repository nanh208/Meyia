// simple admin check (adjust to your original logic if different)
function hasAdminPermission(i) {
  try {
    return i.memberPermissions?.has?.("Administrator") || i.member?.permissions?.has?.("Administrator") || (i.user && i.user.id === OWNER_ID);
  } catch {
    return false;
  }
}

